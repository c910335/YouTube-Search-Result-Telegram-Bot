class YouTube
  attr_accessor :service

  def initialize
    @service = Google::Apis::YoutubeV3::YouTubeService.new
    service.client_options.application_name = 'YouTube Search Result Telegram Bot'
    service.authorization = auth
  end

  def search(q, after)
    ids = service.list_searches(
      'snippet',
      max_results: 50,
      q: q,
      type: 'video',
      published_after: after
    ).items.map { |video| video.id.video_id }

    service.list_videos(
      'snippet,contentDetails',
      id: ids.join(',')
    ).items.map do |video|
      Video.new(
        video.id,
        video.snippet.title,
        video.snippet.channel_id,
        video.snippet.channel_title,
        video.content_details.duration.delete_prefix('PT').downcase
      )
    end.reject { |video| video.duration == 'p0d' }
  end

  def new_playlist(username, query)
    playlist = service.insert_playlist(
      'snippet,status',
      Google::Apis::YoutubeV3::Playlist.new(
        snippet: {
          title: "#{username} #{query}",
          description: "This playlist is auto generated by YTSRTB for #{username} and consists of the search results of \"#{query}\"."
        },
        status: {
          privacy_status: 'unlisted'
        }
      )
    )
    playlist.id
  end

  def delete_playlist(playlist_id)
    service.delete_playlist(playlist_id)
  end

  def clear_playlist(playlist_id, username, query)
    delete_playlist(playlist_id)
    new_playlist(username, query)
  end

  def insert_video(playlist_id, video_id)
    service.insert_playlist_item(
      'snippet',
      Google::Apis::YoutubeV3::PlaylistItem.new(
        snippet: {
          playlist_id: playlist_id,
          resource_id: {
            kind: 'youtube#video',
            video_id: video_id
          }
        }
      )
    )
  rescue Google::Apis::ClientError => e
    p e
    nil
  end

  def channels(channel_ids)
    service.list_channels(
      'id,snippet',
      id: channel_ids.join(',')
    ).items.map do |channel|
      Channel.new(channel.id, channel.snippet.title)
    end
  end

  def auth
    user_id = 'ytsrtb'
    uri = 'urn:ietf:wg:oauth:2.0:oob'
    scope = 'https://www.googleapis.com/auth/youtube.force-ssl'
    client_id = Google::Auth::ClientId.from_file('./client_secret.json')
    token_store = Google::Auth::Stores::FileTokenStore.new(file: 'tokens.yml')
    authorizer = Google::Auth::UserAuthorizer.new(client_id, scope, token_store)
    credentials = authorizer.get_credentials(user_id)
    if credentials.nil?
      url = authorizer.get_authorization_url(base_url: uri)
      puts "Open #{url} in your browser and enter the resulting code:"
      code = gets
      credentials = authorizer.get_and_store_credentials_from_code(
        user_id: user_id, code: code, base_url: uri
      )
    end
    credentials
  end
end
